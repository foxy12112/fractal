#include "text.h"
#include <ctype.h>
#include <stdint.h>

static void	set_glyph(char c, uint8_t glyph[7])
{
	int		index;
	uint8_t	blank[7] = {0, 0, 0, 0, 0, 0, 0};
	uint8_t	font_upper[26][7] = {
		{0x0E, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11},
		{0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E},
		{0x0E, 0x11, 0x10, 0x10, 0x10, 0x11, 0x0E},
		{0x1C, 0x12, 0x11, 0x11, 0x11, 0x12, 0x1C},
		{0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x1F},
		{0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10},
		{0x0F, 0x10, 0x10, 0x17, 0x11, 0x11, 0x0F},
		{0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11},
		{0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x1F},
		{0x01, 0x01, 0x01, 0x01, 0x11, 0x11, 0x0E},
		{0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11},
		{0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1F},
		{0x11, 0x1B, 0x15, 0x15, 0x11, 0x11, 0x11},
		{0x11, 0x19, 0x15, 0x13, 0x11, 0x11, 0x11},
		{0x0E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E},
		{0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10},
		{0x0E, 0x11, 0x11, 0x11, 0x15, 0x12, 0x0D},
		{0x1E, 0x11, 0x11, 0x1E, 0x14, 0x12, 0x11},
		{0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E},
		{0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04},
		{0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E},
		{0x11, 0x11, 0x11, 0x11, 0x11, 0x0A, 0x04},
		{0x11, 0x11, 0x11, 0x15, 0x15, 0x15, 0x0A},
		{0x11, 0x11, 0x0A, 0x04, 0x0A, 0x11, 0x11},
		{0x11, 0x11, 0x0A, 0x04, 0x04, 0x04, 0x04},
		{0x1F, 0x01, 0x02, 0x04, 0x08, 0x10, 0x1F}
	};
	uint8_t	font_digits[10][7] = {
		{0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E},
		{0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E},
		{0x0E, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1F},
		{0x1E, 0x01, 0x01, 0x0E, 0x01, 0x01, 0x1E},
		{0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02},
		{0x1F, 0x10, 0x10, 0x1E, 0x01, 0x01, 0x1E},
		{0x0E, 0x10, 0x10, 0x1E, 0x11, 0x11, 0x0E},
		{0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08},
		{0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E},
		{0x0E, 0x11, 0x11, 0x0F, 0x01, 0x01, 0x0E}
	};

	index = 0;
	while (index < 7)
	{
		glyph[index] = blank[index];
		index++;
	}
	if (c >= 'a' && c <= 'z')
		c = (char)toupper((unsigned char)c);
	if (c >= 'A' && c <= 'Z')
	{
		index = c - 'A';
		while (index >= 0 && index < 26)
		{
			int row = 0;
			while (row < 7)
			{
				glyph[row] = font_upper[c - 'A'][row];
				row++;
			}
			break ;
		}
		return ;
	}
	if (c >= '0' && c <= '9')
	{
		index = 0;
		while (index < 7)
		{
			glyph[index] = font_digits[c - '0'][index];
			index++;
		}
		return ;
	}
	if (c == '+')
	{
		uint8_t plus[7] = {0x00, 0x04, 0x04, 0x1F, 0x04, 0x04, 0x00};
		index = 0;
		while (index < 7)
		{
			glyph[index] = plus[index];
			index++;
		}
	}
	else if (c == '-')
	{
		uint8_t minus[7] = {0x00, 0x00, 0x00, 0x1F, 0x00, 0x00, 0x00};
		index = 0;
		while (index < 7)
		{
			glyph[index] = minus[index];
			index++;
		}
	}
	else if (c == '/')
	{
		uint8_t slash[7] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x00, 0x00};
		index = 0;
		while (index < 7)
		{
			glyph[index] = slash[index];
			index++;
		}
	}
	else if (c == '.')
		glyph[0] = 0x04;
	else if (c == ':')
	{
		glyph[5] = 0x04;
		glyph[1] = 0x04;
	}
	else if (c == ' ')
		return ;
}

static void	draw_pixel(float x, float y, float size)
{
	glBegin(GL_QUADS);
	glVertex2f(x, y);
	glVertex2f(x + size, y);
	glVertex2f(x + size, y + size);
	glVertex2f(x, y + size);
	glEnd();
}

static void	draw_glyph_bitmap(uint8_t glyph[7], float x, float y, float pixel_size)
{
	int	row;
	int	col;

	row = 0;
	while (row < 7)
	{
		col = 0;
		while (col < 5)
		{
			if (glyph[row] & (1 << (4 - col)))
				draw_pixel(x + col * pixel_size, y + (6 - row) * pixel_size, pixel_size);
			col++;
		}
		row++;
	}
}

void	write_text(const char *text, float pixel_size, point start)
{
	float	cursor_x;
	float	cursor_y;
	uint8_t	glyph[7];

	if (text == NULL || pixel_size <= 0.0f)
		return ;
	cursor_x = start.x;
	cursor_y = start.y;
	while (*text != '\0')
	{
		if (*text == '\n')
		{
			cursor_x = start.x;
			cursor_y -= 8.0f * pixel_size;
			text++;
			continue ;
		}
		set_glyph(*text, glyph);
		draw_glyph_bitmap(glyph, cursor_x, cursor_y, pixel_size);
		cursor_x += 6.0f * pixel_size;
		text++;
	}
}
